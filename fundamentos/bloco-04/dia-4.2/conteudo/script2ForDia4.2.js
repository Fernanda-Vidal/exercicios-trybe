// Apresentar o conteúdo do Array sem o For. 
let cars = ['Saab', 'Volvo', 'BMW'];
console.log(cars[0]);
console.log(cars[1]);
console.log(cars[2]);

// Apresentar o conteúdo do Array usando o For.
let cars = ['Saab', 'Volvo', 'BMW'];

for (let index = 0; index < cars.length; index += 1){
    console.log(cars[index]);
}
